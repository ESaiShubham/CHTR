// Helper functions to simulate API calls
export const getFeaturedPlaces = () => {
  return Promise.resolve(placesData.slice(0, 6)); // Return first 6 places as featured
};

export const getAllPlaces = () => {
  return Promise.resolve(placesData);
};

export const getPlaceById = (id) => {
  const place = placesData.find(p => p._id === id);
  return Promise.resolve(place);
};

export const searchPlaces = (query) => {
  const filtered = placesData.filter(place =>
    place.name.toLowerCase().includes(query.toLowerCase()) ||
    place.location.toLowerCase().includes(query.toLowerCase()) ||
    place.description.toLowerCase().includes(query.toLowerCase())
  );
  return Promise.resolve(filtered);
};

export const getPlacesByCategory = (category) => {
  if (category === 'all') {
    return Promise.resolve(placesData);
  }
  const filtered = placesData.filter(place => place.category === category);
  return Promise.resolve(filtered);
}; 